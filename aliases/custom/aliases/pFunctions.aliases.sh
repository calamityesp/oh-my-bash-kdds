#------------------------- Ideavim Functions ----------------------------/
#-------------------------------------------------------------------------/
function idea() {
  project=$1
  if [[ -z $project ]]; then
    idea . > /dev/null 2>&1 &
  else
    idea $project >/dev/null 2<&1 &
  fi
}


