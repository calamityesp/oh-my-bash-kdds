#-------------------------------------------------------------------------/
#-------------------------- Git Command Functions---------------------------/
#-------------------------------------------------------------------------/
function gitp() {
  echo -n "Commit Message: "
  read commitmsg
  git add .
  git commit -m "$commitmsg"
  git push
  echo "Push Complete"
}


# ohmybash related functions
function gitcommitcustom() {
  echo -n "Commit Message: "
  read commitmsg
  cd $ohmybashcustompath
  git add .
  git commit -m "$commitmsg"
  git push
  cd -
}



